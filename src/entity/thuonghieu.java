package entity;

/**
 *
 * @author Admin
 */
public class thuonghieu {
    int id;
    String ten;
    String mota;
    boolean trangthai;

    public thuonghieu() {
    }

    public thuonghieu(int id, String ten, String mota, boolean trangthai) {
        this.id = id;
        this.ten = ten;
        this.mota = mota;
        this.trangthai = trangthai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public boolean isTrangthai() {
        return trangthai;
    }

    public void setTrangthai(boolean trangthai) {
        this.trangthai = trangthai;
    }
    
    
}
