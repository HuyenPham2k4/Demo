public class danhmuc {
    int id;
    String ten;
    boolean trangthai;

    public danhmuc() {
    }

    public danhmuc(int id, String ten, boolean trangthai) {
        this.id = id;
        this.ten = ten;
        this.trangthai = trangthai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public boolean isTrangthai() {
        return trangthai;
    }

    public void setTrangthai(boolean trangthai) {
        this.trangthai = trangthai;
    }
    
}
